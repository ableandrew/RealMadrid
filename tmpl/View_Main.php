    Ференц Пушкаш, Хосе Эмилио Сантамария, Пауль Брайтнер, Грегорио Бенито, Хосе Антонио Камачо, Мигель Чендо, Эмилио Бутрагеньо,
    Зинедин Зидан, Луиш Фигу, Фернандо Йерро, Роналдо, Дэвид Бекхэм. С клубом работало немало известных тренеров: Сантьяго Бернабеу,
    Франсиско Бру, Мигель Муньос, Вуядин Бошков, Альфредо Ди Стефано, Лео Бенхаккер, Джон Тошак, Радомир Антич, Висенте Дель Боске,
    Гус Хиддинк, Юпп Хайнкес, Фабио Капелло, Хосе Антонио Камачо, Вандерлей Лушембургу, Бернд Шустер, Хуанде Рамос.</h2><br>

